// Rules for variables.
// 1) Cannot be a reversed keyword like let, if .. 
// 2) Should be meaningful names
// 3) Cannot contain space or hypen (-)
// 4) Cannot start with a number (1name)
// 5) Variables name are case-sensitive (firstName and firstname is not the same)
// 6) Use camel notation, start with small letter and second word starts with upper case (firstName)
// 7) Declare each variable in a new line.

let firstName = John;
let lastName = Doe;
